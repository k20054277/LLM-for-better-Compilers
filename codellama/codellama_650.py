# Demonstrate the use of True and complex

# Define a variable x as True
x = True
print(x) # Output: True

# Define a variable y as a complex number
y = 3 + 4j
print(y) # Output: (3+4j)

# Use the `==` operator to compare x and y
if x == y:
    print("x and y are equal")
else:
    print("x and y are not equal")

# Use the `!=` operator to compare x and y
if x != y:
    print("x and y are not equal")
else:
    print("x and y are equal")