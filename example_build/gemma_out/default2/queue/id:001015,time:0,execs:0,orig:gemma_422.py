
# Define a list of numbers
numbers = [1, 2, 3, 4, 5]

# Iterate over the list and print each element
for num in numbers:
    print(num)

# Check if a number is even using False as a comparison
for num in numbers:
    if num % 2 == 0:
        print(num + " is even")
    else:
        print(num + " is not even")
