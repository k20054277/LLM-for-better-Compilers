
# This Python program demonstrates the use of as and chr.

# Define a variable and assign it to a character.
my_char = chr(65)

# Print the character.
print(my_char)

# Convert the character back to an integer.
my_int = int(my_char)

# Print the integer.
print(my_int)

# Use the as keyword to bind a variable to a temporary alias.
as_char = chr(65)

# Print the character.
print(as_char)

# Convert the character back to an integer.
as_int = int(as_char)

# Print the integer.
print(as_int)
