
# This Python program demonstrates the use of False and None

# Define a function that returns False
def is_even(num):
    return num % 2 == 0

# Check if 6 is even using the is_even function
if is_even(6):
    print("6 is even")

# Check if None is False
if None is False:
    print("None is False")

# Print the truth value of False
print(False)
