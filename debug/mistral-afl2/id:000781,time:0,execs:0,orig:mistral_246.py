
# Define a variable with False value
is_raining = False

if not is_raining:
    print("It's a sunny day!")
else:
    print("It's raining today.")

# Another example using a boolean expression
num = 10
is_even = num % 2 == 0

if is_even:
    print(num, "is an even number")
else:
    print(num, "is an odd number")
