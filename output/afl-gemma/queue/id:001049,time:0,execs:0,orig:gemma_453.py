
# False and Refactoring

# Original code
print(5 > 3)  # Output: True
print(7 < 4)  # Output: False

# Refactoring using False
print(not 5 > 3)  # Output: False
print(4 >= 7)  # Output: False

# Further refactoring using Boolean not
print(not 5 > 3)  # Output: False
print(4 >= 7)  # Output: False

# Output
# False
# False
# False
# False
