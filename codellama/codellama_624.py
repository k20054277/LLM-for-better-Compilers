# Using True and is

# Create a variable called "x" with a value of 10
x = 10

# Create a variable called "y" with a value of 10
y = 10

# Check if x and y are equal using ==
if x == y:
    print("x and y are equal")
else:
    print("x and y are not equal")

# Check if x is True using is
if x is True:
    print("x is True")
else:
    print("x is not True")