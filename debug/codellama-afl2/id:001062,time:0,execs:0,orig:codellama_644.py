# Test if a number is positive using True and bool
number = 5
if number > 0:
    print("Number is positive")
else:
    print("Number is not positive")

# Use True and bool to check if a variable is true or false
x = False
y = True
if x == True:
    print("x is true")
else:
    print("x is false")
if y == True:
    print("y is true")
else:
    print("y is false")