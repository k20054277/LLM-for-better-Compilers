
# Logical 'and' example

def main():
    # Define some variables
    x = 10
    y = 20

    # Use the and operator to test two conditions
    if (x > 5) and (y > 15):
        print("Condition 1 and Condition 2 are true.")
    else:
        print("One or both of the conditions are false.")

if __name__ == "__main__":
    main()
