
# This Python program demonstrates the use of True and testing

# Define a function to test if a number is even
def is_even(num):
  return num % 2 == 0

# Test if 6 is even
if is_even(6):
  print("6 is even")

# Test if 7 is even
if not is_even(7):
  print("7 is not even")

# True and False tests
if True:
  print("True is true")

if False:
  print("False is false")
