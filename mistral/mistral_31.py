
def is_negative(num):
    if num < 0:
        return False
    else:
        return None

# Testing the function
print(is_negative(-5))   # prints: False
print(is_negative(5))    # prints: None
print(is_negative(-1))   # prints: False
print(is_negative(0))    # prints: None
print(is_negative(3))    # prints: None
