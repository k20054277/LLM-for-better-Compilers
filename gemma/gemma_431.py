
# False and Loop Demonstration

# Create a list of numbers from 1 to 10
numbers = range(1, 11)

# Iterate over the list using a loop
for number in numbers:
    # Check if the number is divisible by 2
    if number % 2 == 0:
        # Print the number
        print(number)

# Print the message "Finished"
print("Finished")
