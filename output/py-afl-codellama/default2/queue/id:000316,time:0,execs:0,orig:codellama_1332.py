# Printing the value using assert statement
assert 5 == 5, "Values are not equal"
print(repr(5)) # prints '5'

# Printing the value using repr function
print(repr(5)) # prints '5'

# Printing a list using assert statement
assert [1, 2, 3] == [1, 2, 3], "Lists are not equal"
print(repr([1, 2, 3])) # prints '[1, 2, 3]'

# Printing a list using repr function
print(repr([1, 2, 3])) # prints '[1, 2, 3]'