# Using and
age = 18
if age >= 18 and age <= 25:
    print("You are eligible for a driver's license.")
else:
    print("You are not eligible for a driver's license.")

# Using &
age = 18
if (age >= 18) & (age <= 25):
    print("You are eligible for a driver's license.")
else:
    print("You are not eligible for a driver's license.")