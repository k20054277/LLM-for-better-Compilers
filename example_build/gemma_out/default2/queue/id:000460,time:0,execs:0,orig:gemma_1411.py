
# This Python program demonstrates the use of assert and type

# Define a function called square
def square(x):
    return x ** 2

# Assert that square(5) is equal to 25
assert square(5) == 25

# Print the result of square(5)
print(square(5))

# Check the type of square(5)
print(type(square(5)))

# Output:
# 25
# <class 'int'>
