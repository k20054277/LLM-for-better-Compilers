# Test if a number is even or odd using the % operator
num = 1234
if num % 2 == 0:
    print("Even")
else:
    print("Odd")

# Test if a string contains a substring
str = "hello world"
if "world" in str:
    print("Yes, 'world' is in the string.")
else:
    print("No, 'world' is not in the string.")