# Print "False" if the variable "x" is not equal to 10, otherwise print "From".
x = 5
if x == 10:
    print("From")
else:
    print("False")

# Print "False" if the value of the variable "y" is not in the range of 0-10, otherwise print "From".
y = 5
if y in range(0, 10):
    print("From")
else:
    print("False")