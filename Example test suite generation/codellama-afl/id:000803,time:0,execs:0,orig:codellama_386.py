# This program demonstrates the use of False and *=

x = 5
y = 10

if x < y:
    print("x is less than y")
else:
    print("x is greater than or equal to y")

# Assigning False to x
x = False

print(x) # Output: False

# Assigning 5 to y
y = 5

if x < y:
    print("x is less than y")
else:
    print("x is greater than or equal to y")