
# Define a list of numbers
numbers = [1, 2, 3, 4, 5]

# Use as and enumerate to iterate over the list
for number, index in enumerate(numbers):
    print(f"Number: {number}, Index: {index}")

# Output
# Number: 0, Index: 0
# Number: 1, Index: 1
# Number: 2, Index: 2
# Number: 3, Index: 3
# Number: 4, Index: 4
