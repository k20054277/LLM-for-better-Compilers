# This program demonstrates the use of assert and syntax in Python

def main():
    # Assert statements can be used to test for specific conditions
    assert 5 > 2, "5 is greater than 2"
    assert 5 < 10, "5 is less than 10"

    # Syntax errors are also detected at runtime
    print("Hello, World!")

if __name__ == "__main__":
    main()