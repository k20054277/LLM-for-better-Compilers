
# False and method demonstration

# Define a method to check if a number is even
def is_even(num):
  return num % 2 == 0

# Check if 6 is even using the is_even method
if is_even(6):
  print("6 is even")

# Check if 7 is even using the is_even method
if is_even(7):
  print("7 is even")

# Output
# 6 is even
# 7 is not even
