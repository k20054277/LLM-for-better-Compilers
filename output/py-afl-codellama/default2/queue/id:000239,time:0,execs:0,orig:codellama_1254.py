# Using 'as' to create an alias for a variable
a = 5
b = a + 2
c = b * 3
print(c)  # Output: 15

# Using 'or' with 'if' statement
x = None
y = "hello"
if x or y:
    print("Either x or y is not None")

# Using 'or' with a boolean expression
if x is None or y == "hello":
    print("Either x is None or y equals hello")