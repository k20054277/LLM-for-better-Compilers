
# This Python program demonstrates the use of None and =

# Declare a variable and assign it to None
a = None

# Print the value of a
print(a)

# Assign a value to a
a = 10

# Print the value of a
print(a)

# Comparison operators with None
if a is None:
    print("a is None")

if a is not None:
    print("a is not None")

# Equality operator with None
if a == None:
    print("a is None")

# Assignment operator with None
a = None

# Print the value of a
print(a)
