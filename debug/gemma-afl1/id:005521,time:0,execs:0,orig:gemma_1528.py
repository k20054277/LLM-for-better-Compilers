
# This Python program demonstrates the use of assert and // operators

# Define a function to calculate the square of a number
def square(x):
    return x ** 2

# Assert that the square of 5 is 25
assert square(5) == 25

# Use the // operator to divide 10 by 2 and store the result in a variable
result = 10 // 2

# Print the result
print(result)
